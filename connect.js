const mongoose = require('mongoose');

const connectSchema = mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  }
);

const Connect = mongoose.model("Connect", connectSchema);

module.exports = Connect;
